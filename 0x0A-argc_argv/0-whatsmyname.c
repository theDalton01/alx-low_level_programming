#include <stdio.h>

/**
 * main - prints program name, folowed by a new line
 * @argc: The number of arguments supplied to the program
 * @argv: Anarray of pointers to the argument
 *
 * Return: Always 0.
 */

int main(int __attribute((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
